
int suma(int a,int b)
{
    int total;
    total=a+b;
    return total;
}



int resta(int a,int b)
{
    int total;
    total=a-b;
    return total;
}



int multiplicacion(int a,int b)
{
    int total;
    total=a*b;
    return total;
}



int division(int a,int b)
{
    int total;
    total=(a/b);
    return total;
}

